package gui;
/**<b>Кнопка вывода информации о разрабах</b>
 * @author Arslan Khusainov
 * класс основана на абстракции кнопки
 */
public final class ButtonAboutUs extends AbstractButton{
	private static final long serialVersionUID = 1364186618766170973L;
	/** Окно "О нас"*/
	private AboutUsFrame aboutUsFrame = new AboutUsFrame();
	/** Конструктор*/
	public ButtonAboutUs(String name) {
		super(name);
		
	}
	@Override
	final protected void buttonAction() {
		aboutUsFrame.setVisible(true);
	}
	

}
